package loops;

public class LoopsCount {

	public static void main(String[] args) {

		System.out.println("this is simple op");

		System.out.println("this is simple op");
		System.out.println("this is simple op");
		System.out.println("this is simple op");
		System.out.println("this is simple op");
		
		
		
		int count=0;
		for(int i=100;i<=500;i++) // 6<=5  false
		{
			count++;    // 5
		}

		System.out.println(count);
	}

}
