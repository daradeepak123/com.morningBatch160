package dataTypesVariables;

public class DataTypesLearn {
	
	
	public static void printByteValue()
	{
		byte b=100;
		System.out.println("this is byte value "+b);
	}
	
	public static void printShortValue()
	{
		short b=10000;
		System.out.println("this is short value "+b+"  post the b value");
	}
	
	public static void printIntValue()
	{
		int b=100000000;
		System.out.println("this is int value "+b);
	}
	
	public static void printLongValue()
	{
		long b=100000000000000l;
		System.out.println("this is long value "+b);
	}
	
	
	public static void main(String args[])
	{
		printByteValue();
		printShortValue();
		printIntValue();
		printLongValue();
	}
	

}
